/**
 * Enumeración que define las claves de almacenamiento utilizadas en la aplicación.
 */
export enum StorageKeyEnum {
  /**
   * Clave de almacenamiento para el usuario.
   */
  User = 'usuario',
  /**
   * Clave de almacenamiento para el token.
   */
  Token = 'token',
  /**
   * Clave de almacenamiento para el tema.
   */
  Theme = 'theme',
  /**
   * Clave de almacenamiento para el rol.
   */
  Role = 'rol',
  /**
   * Clave de almacenamiento para el proveedor de identidad oauth.
   */
  OAuth2 = 'oauth2',
}
